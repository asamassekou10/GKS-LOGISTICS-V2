// Current language
let currentLanguage = 'fr';

// Update language function
function updateLanguage() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
        }
    });
}

// DOM Content Loaded
document.addEventListener('DOMContentLoaded', function() {
    // Initial language update
    updateLanguage();
    
    // Language toggle functionality
    const languageToggle = document.getElementById('languageToggle');
    if (languageToggle) {
        languageToggle.addEventListener('click', function() {
            currentLanguage = currentLanguage === 'fr' ? 'en' : 'fr';
            updateLanguage();
        });
    }
    
    // Mobile menu toggle
    const menuToggle = document.querySelector('.menu-toggle');
    const nav = document.querySelector('.nav');
    
    if (menuToggle && nav) {
        menuToggle.addEventListener('click', function() {
            nav.classList.toggle('active');
        });
    }
    
    // Header scroll effect
const header = document.querySelector('.header');
    if (header) {
        window.addEventListener('scroll', function() {
            if (window.scrollY > 100) {
    header.classList.add('scrolled');
  } else {
    header.classList.remove('scrolled');
  }
});
    }
    
    // Back to top button
const backToTop = document.querySelector('.back-to-top');
    if (backToTop) {
        window.addEventListener('scroll', function() {
  if (window.scrollY > 300) {
    backToTop.classList.add('visible');
  } else {
    backToTop.classList.remove('visible');
  }
});

        backToTop.addEventListener('click', function() {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });
    }
    
    // Smooth scrolling for "View Job Openings" button
    const viewJobsBtn = document.querySelector('.hero-cta-btn');
    if (viewJobsBtn) {
        viewJobsBtn.addEventListener('click', function(e) {
            e.preventDefault();
            const jobSection = document.querySelector('.job-opportunities-section');
            if (jobSection) {
                jobSection.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    }
    
    // Newsletter form submission for careers page
    const careersNewsletterForm = document.getElementById('careersNewsletterForm');
    if (careersNewsletterForm) {
        careersNewsletterForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const email = this.querySelector('input[type="email"]').value;
            if (email) {
                alert(translations[currentLanguage]['newsletter-subscribe'] + ' - Thank you for subscribing!');
                this.reset();
            }
        });
    }
    
    // Spontaneous application button
    const spontaneousBtn = document.querySelector('.spontaneous-application-button');
    if (spontaneousBtn) {
        spontaneousBtn.addEventListener('click', function(e) {
            e.preventDefault();
            // For now, just show an alert - can be enhanced to open application form
            alert(translations[currentLanguage]['spontaneous-application-button'] + ' - Feature coming soon!');
        });
    }
    
    // Contact button functionality
    const contactButtons = document.querySelectorAll('.contact-btn');
    contactButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            // Navigate to contact page or open contact form
            window.location.href = 'contact.html';
        });
    });
    
    // Social media links
    const socialLinks = document.querySelectorAll('.social-icons a');
    socialLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const platform = this.querySelector('i').className;
            let url = '#';
            
            if (platform.includes('linkedin')) {
                url = 'https://linkedin.com/company/gks-logistics';
            } else if (platform.includes('twitter')) {
                url = 'https://twitter.com/gkslogistics';
            } else if (platform.includes('facebook')) {
                url = 'https://facebook.com/gkslogistics';
            }
            
            window.open(url, '_blank');
        });
    });
    
    // Newsletter form submission
    const newsletterForm = document.querySelector('.newsletter-form');
    if (newsletterForm) {
        newsletterForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const email = this.querySelector('input[type="email"]').value;
            if (email) {
                alert(translations[currentLanguage]['footer-newsletter-button'] + ' - Thank you for subscribing!');
                this.reset();
            }
        });
    }
});